<h4>Parliamentary Affairs Committee</h4>

The Parliamentary Affairs Committee is responsible for deciding the

agenda of the Parliament meetings. It also server the purpose of  amending and bringing 

up-to-date all rules, procedures, structures, norms, convention, codes-of-conduct, etc. as 

well as study all proposed changes in these rules and procedures and give its opinion on

the same.

<h4>Finance Committee</h4>

The Principal aim of the Finance Committee is to manage the finances of

the Gymkhana. It controls and supervises the expenditures incurred by the

executive wings of the Gymkhana, viz. The Cultural Council, The Science & Technology 

Council, The Games & Sports Council and The Film & Media Council.

<h4>Emergency Committee</h4>

Emergency Committee represents the Parliament in case of any emergency

since the Parliament meeting cannot be called up in short time.

<h4>Festivals Committee</h4>

Festivals Committee is responsible for all matters except financial matters related to 

festivals organized by Gymkhana. All preliminary reports related to festivals are 

submitted to this committee.

<h4>Nominations Committee</h4>

The Nominations Committee is responsible for nominating students for various institute 

bodies.

<h4>Hostel Affairs & General Welfare Committee</h4>

The Hostel Affairs & General Welfare Committee looks after the matters related to 

hostels and general welfare of students.

<h4>Security Committee</h4>

The Security Committee serves as a student representative body to the Institute Proctorial 

Board. It oversees security arrangements in campus at all times and address

students’ concerns and issues regarding crime

<h4>Students’ Placement Committee</h4>

The primary aim of the Students’ Placement Committee is to express

students’ opinion regarding placements and it acts as the students’ central

advisory body for the Training and Placement Cell.

<h4>Web Management Committee</h4>

The Web Management Committee manages websites of Gymkhana, festivals and student 

related activities. It is also  responsible for managing all content of website.

Undergraduate Students’ Academic Affairs Committee

It advices and assists the Parliament on matters related to the undergraduate

academic program.

Postgraduate Students’ Academic Affairs Committee

It advices and assists the Parliament on matters related to postgraduate

academic program.

<h4>Alumni Relations Committee</h4>

Alumni Relations Committee assists the Dean of Resources and Alumni Affairs in

improving student-alumni interaction. It collects information regarding

alumni and serves as a critical link between the Institute and alumni.

<h4>Grievance Redressal and Enquiries Committee</h4>

Grievance Redressal and Enquiries Committee addresses grievances of General Body and 

acts as a source for providing information to the general body.

